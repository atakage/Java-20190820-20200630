package com.biz.var;

public class IntVar_02 {
	
	public static void main(String[] args) {
		
		int intNum1 = 0;
		int intNum2 = 0;
		int intSum = 0;
		
		intNum1 = 40;
		intSum = intNum1 + intNum2;
		System.out.println(intSum);
		
		intNum2 = 50;
		intSum = intNum1 + intNum2;
		System.out.println(intSum);
		
		intSum = 100 + 200;
		System.out.println(intSum);
		
		intSum = 10 * 20;
		intSum = 200 / 10;
	}

}

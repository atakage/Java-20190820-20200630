package com.biz.var;

public class Var_05 {
	
	public static void main(String[] args) {
		
		
		
		int num1 = 0;
		
		
		num1 = num1 + 10;
		num1 = num1 + 5;
		
		System.out.println(num1);
		
		
		
		/*
		 * num1에 저장된 값(0)을 cpu로 복제
		 * 숫자 10을 cpu로 복제
		 * 두 값(0,10)을 덧셈
		 * 그 결과를 num1 변수에 저장(num1의 값을 10 증가 시켜라)
		 * 
		 * num1에 저장된 값(10)을 cpu로 복제
		 * 숫자 5를 cpu로 복제
		 * 두 값(10,5)을 덧셈
		 * 그 결과를 num1 변수에 저장(num1의 값을 5 증가 시켜라)
		 * 
		 */
	}

}
